package maintenance

func RunDetail(run Run) map[string]any {
	if run.Detail == nil {
		return map[string]any{}
	}
	return run.Detail
}

func Int64FromDetail(value any) int64 {
	switch typed := value.(type) {
	case int64:
		return typed
	case int:
		return int64(typed)
	case float64:
		return int64(typed)
	default:
		return 0
	}
}

func IntFromDetail(value any, fallback int) int {
	switch typed := value.(type) {
	case int:
		return typed
	case int64:
		return int(typed)
	case float64:
		return int(typed)
	default:
		return fallback
	}
}
